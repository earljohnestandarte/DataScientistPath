# Numbers: int (arbitrary-precision integers), float (64-bit)
#
# Boolean: True and False (note the capital 'T' and 'F')
#
# The "Null" Type: None (also capitalized)
#
# Strings: str. They are immutable.

# Type Hinting
num: int = 10
decimal: float = 3.14
word: str = "Hello"
boolean: bool = True
null: None = None

# Outputting Types
print(type(num))
print(type(decimal))
print(type(word))
print(type(boolean))
print(type(null))

# Outputting values
print(num)
print(decimal)
print(word)
print(boolean)
print(null)


